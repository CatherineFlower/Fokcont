package ru.fokcont.app.ui.policy

import android.os.Bundle
import android.view.View
import androidx.fragment.app.Fragment
import androidx.navigation.fragment.findNavController
import ru.fokcont.app.R
import ru.fokcont.app.databinding.FragmentPolicyBinding

class PolicyFragment : Fragment(R.layout.fragment_policy) {

    private var _binding: FragmentPolicyBinding? = null
    private val binding get() = _binding!!

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        _binding = FragmentPolicyBinding.bind(view)

        val type = arguments?.getString("policy_type") ?: "privacy"
        
        if (type == "privacy") {
            binding.policyTitleText.text = "Политика конфиденциальности"
            binding.policyContentText.text = """
                ПОЛИТИКА КОНФИДЕНЦИАЛЬНОСТИ ПРИЛОЖЕНИЯ «ФОККОНТ»
                
                Дата вступления в силу: 1 января 2024 г.
                
                1. ОБЩИЕ ПОЛОЖЕНИЯ
                Настоящая Политика определяет порядок обработки и защиты персональной информации пользователей приложения «Фокконт». Ваша приватность является нашим приоритетом.
                
                2. КАКИЕ ДАННЫЕ МЫ СОБИРАЕМ
                • Информация о сессиях: название задач, время начала и окончания, заметки.
                • Статистика использования приложений: данные о переключениях между окнами (через UsageStats API). Пользователь самостоятельно выбирает список контролируемых приложений в настройках. Обратите внимание, что популярные отвлекающие приложения (YouTube, VK, Telegram и др.) включаются в список мониторинга автоматически при их обнаружении на устройстве.
                • Безопасность: хэш вашего PIN-кода (сам PIN не хранится).
                
                3. ЛОКАЛЬНОЕ ХРАНЕНИЕ И ШИФРОВАНИЕ
                Все собираемые данные хранятся ИСКЛЮЧИТЕЛЬНО на вашем мобильном устройстве. Приложение не имеет серверной части и не передает ваши данные в облако или третьим лицам.
                Для защиты данных используется стандарт шифрования AES-256.
                
                4. ЦЕЛИ ОБРАБОТКИ
                Данные собираются только для предоставления вам аналитики вашей продуктивности и визуализации графиков отвлечений внутри приложения.
                
                5. УДАЛЕНИЕ ДАННЫХ
                Вы можете полностью удалить все данные приложения в любой момент через настройки приложения или удалив само приложение с устройства.
            """.trimIndent()
        } else {
            binding.policyTitleText.text = "Согласие на обработку данных"
            binding.policyContentText.text = """
                СОГЛАСИЕ НА ОБРАБОТКУ ПЕРСОНАЛЬНЫХ ДАННЫХ
                
                Используя приложение «Фокконт», вы даете свое согласие на следующую обработку данных:
                
                1. ТИП РАЗРЕШЕНИЯ
                Приложению требуется доступ к системной функции «Доступ к статистике использования» (Usage Access). Это необходимо для фиксации моментов, когда вы покидаете рабочее приложение и переключаетесь на социальные сети или игры.
                
                2. ПЕРЕЧЕНЬ ДЕЙСТВИЙ
                Приложение будет фиксировать:
                • Название активного приложения в момент работы таймера.
                • Время переключения между приложениями.
                
                3. УСЛОВИЯ БЕЗОПАСНОСТИ
                • Обработка происходит в автоматическом режиме без участия человека.
                • Полученные данные не привязываются к вашей личности (имени, телефону, почте).
                • Данные остаются в зашифрованном хранилище вашего телефона.
                
                4. ОТЗЫВ СОГЛАСИЯ
                Вы можете отозвать данное согласие в любой момент, отключив разрешение Usage Access в системных настройках Android. В этом случае функция автоматического мониторинга отвлечений станет недоступна.
            """.trimIndent()
        }

        binding.backButton.setOnClickListener {
            findNavController().popBackStack()
        }
        
        binding.closeButton.setOnClickListener {
            findNavController().popBackStack()
        }
    }

    override fun onDestroyView() {
        super.onDestroyView()
        _binding = null
    }
}
